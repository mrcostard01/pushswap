#include "libft.h"

int	ft_isalpha(int c)
{
	int	istrue;

	if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90))
		istrue = 1;
	else
	{
		istrue = 0;
		return (istrue);
	}
	return (istrue);
}
