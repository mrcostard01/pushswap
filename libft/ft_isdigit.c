#include "libft.h"

int	ft_isdigit(int c)
{
	int	istrue;

	istrue = 0;
	if (c >= 48 && c <= 57)
		istrue = 1;
	else
	{
		istrue = 0;
		return (istrue);
	}
	return (istrue);
}
